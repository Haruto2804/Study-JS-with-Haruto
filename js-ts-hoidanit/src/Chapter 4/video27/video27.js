const scores = 10;
switch (true) {
    case (scores >= 8 && scores <= 10):
        console.log("Giỏi!!!");
        break;
    case (scores >= 6 && scores < 8):
        console.log("Khá");
        break;
    case (scores >= 4 && scores < 6):
        console.log("Trung Bình!!");
        break;
    default:
        console.log("Kém!!");
}